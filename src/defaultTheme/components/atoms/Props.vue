<template>
  <TableContainer>
    <table v-if="component">
      <thead>
        <tr>
          <th>Prop</th>
          <th>Type</th>
          <th v-if="showRequired">Required</th>
          <th v-if="showDefault">Default</th>
          <th v-if="showValues">Values</th>
          <th v-if="showDescription">Description</th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="prop in properties" :key="prop.name">
          <td>
            <code>{{ prop.name }}</code>
          </td>
          <td>
            <code>{{ prop.type && prop.type.name }}</code>
          </td>
          <td v-if="showRequired">{{ prop.required ? 'Yes' : 'No' }}</td>
          <td v-if="showDefault">
            <code v-if="prop.defaultValue">{{ prop.defaultValue && prop.defaultValue.value }}</code>
          </td>
          <td v-if="showValues">
            <code v-if="prop.values">{{ prop.values && JSON.stringify(prop.values).replace(/,/g, ', ') }}</code>
            <span v-else>-</span>
          </td>
          <td v-if="showDescription">
            <div v-html="prop.description"></div>
          </td>
        </tr>
      </tbody>
    </table>
  </TableContainer>
</template>

<script>
import { computed, defineComponent } from '@nuxtjs/composition-api'

export default defineComponent({
  props: {
    of: {
      type: String,
      default: undefined
    },
    /**
     * @ignore
     */
    data: {
      type: Object,
      default: () => ({})
    },
    /**
     * Toggle required column.
     */
    required: {
      type: Boolean,
      default: undefined
    },
    /**
     * Toggle values column.
     */
    values: {
      type: Boolean,
      default: undefined
    },
    /**
     * Toggle description column.
     */
    description: {
      type: Boolean,
      default: undefined
    },
    /**
     * Toglle default column.
     */
    defaultValue: {
      type: Boolean,
      default: undefined
    }
  },
  setup(props) {
    const component = computed(() => props.data)

    const properties = computed(() => component.value.props.filter(prop => !prop.tags?.ignore))

    const showRequired = computed(() => {
      if (props.required !== undefined) {
        return props.required
      }

      return properties.value.find(prop => prop.required !== undefined)
    })

    const showValues = computed(() => {
      if (props.values !== undefined) {
        return props.values
      }

      return properties.value.find(prop => prop.values)
    })

    const showDescription = computed(() => {
      if (props.description !== undefined) {
        return props.description
      }

      return properties.value.find(prop => prop.description)
    })

    const showDefault = computed(() => {
      if (props.defaultValue !== undefined) {
        return props.defaultValue
      }

      return properties.value.find(prop => prop.defaultValue)
    })

    return {
      component,
      properties,
      showRequired,
      showValues,
      showDescription,
      showDefault
    }
  }
})
</script>
